namespace Elements.Annotations
{
    /// <summary>
    /// The severity of a given message.
    /// </summary>
    public enum MessageSeverity
    {
        /// <summary>
        /// Message is for information purposes only.
        /// </summary>
        Info,

        /// <summary>
        /// Message is for a warning.
        /// </summary>
        Warning,

        /// <summary>
        /// Message is for an error.
        /// </summary>
        Error
    };
}